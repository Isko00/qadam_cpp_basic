#include <iostream>
using namespace std;

int main()
{
    for (int i = 0; i < 8; i++) {  
        cout << "\ti[" << i << "]" << endl; 
        for (int j = 0; j < 8; j++) {        
            cout << "j[" << j << "] " << endl;      
        }
        for (int k = 0; k < 10; k++) {
            cout << "k[" << k << "] " << endl;
        }
        
    }
        

        
    return 0;
}